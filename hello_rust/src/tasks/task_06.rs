/**
 * run - Check if a person is an adult
 *
 * Description: Uses a conditional statement to determine
 * if someone is of legal age
 */
pub fn run() {
    let age = 18;

    if age >= 18
    {
        println!("You are an adult");
    }
    else 
    {
        println!("You are a minor");
    }
}
